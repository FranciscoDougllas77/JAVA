//Operadores Aritmeticos em Java
// Em Java, os operadores aritméticos são usados para realizar operações matemáticas básicas.
// Os principais operadores aritméticos são: + (adição), - (subtração), * (multiplicação), / (divisão) e % (módulo).
public class aritmeticos {
    public static void main(String[] args){
        // Declaração de variáveis
        int a = 10;
        int b = 5;

        // Adição
        int soma = a + b;
        System.out.println("Soma: " + soma);

        // Subtração
        int subtracao = a - b;
        System.out.println("Subtração: " + subtracao);

        // Multiplicação
        int multiplicacao = a * b;
        System.out.println("Multiplicação: " + multiplicacao);

        // Divisão
        int divisao = a / b;
        System.out.println("Divisão: " + divisao);

        // Módulo (resto da divisão)
        int modulo = a % b;
        System.out.println("Módulo: " + modulo);
    }
}
