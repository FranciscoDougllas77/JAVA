public class for_Java {
    public static void main(String[] args){
        //Estrutura de Repeticao for

        for(int i = 1; i <= 10; i ++){
            System.out.printf("%d + %d = %d%n", i, 1, i+1);
        }

        System.out.println("------------------");
        for(int i = 10; i >= 1; i --){
            System.out.printf("%d - %d = %d%n", i , 1, i-1);
        }
    }
    
}
