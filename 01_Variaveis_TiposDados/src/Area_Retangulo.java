//Calculo a área de um retângulo
// A área de um retângulo é calculada multiplicando a base pela altura.

public class Area_Retangulo {
    public static void main(String[] args){
        double base = 5.0;
        double altura = 3.5;

        double area = base * altura;

        System.out.println("A área do retângulo é: " + area + " unidades quadradas.");
    }
    
}
