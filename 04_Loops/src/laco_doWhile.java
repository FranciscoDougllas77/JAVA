// do while em Java
// O loop do while é utilizado para executar um bloco de código pelo menos uma vez,
// e depois continuar executando enquanto uma condição for verdadeira.  

public class laco_doWhile {
    public static void main(String[] args){
        int contador = 1;

        do{
            System.out.println("O Valor atual e: "+contador);
            contador ++;
            // O loop começa com contador = 1, e continua enquanto
        }while(contador <= 10);
        // O loop começa com contador = 1, e continua enquanto
    }
    
}
