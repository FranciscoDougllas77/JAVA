//Loop for em Java
// O loop for é utilizado para executar um bloco de código um número específico de vezes.
public class loop_for {
    public static void main(String[] args){
        for(int i = 0; i <= 10; i ++){
            System.out.println("O Valor atual e: " + i);
            // O loop começa com i = 0, e continua enquanto
            // i for menor ou igual a 10.
        }
    }
}
